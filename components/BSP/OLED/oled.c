void oled_draw_string(uint8_t x, uint8_t y, const char *str) {
    while (*str) {
        uint8_t c = *str - 32;
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                if (font6x8[c][i] & (1 << j)) {
                    oled_draw_pixel(x + i, y + j);
                }
            }
        }
        x += 8;
        str++;
    }
}